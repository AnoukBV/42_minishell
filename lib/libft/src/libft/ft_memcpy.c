#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	char			*dest2;
	const char		*src2;
	unsigned int	i;

	if (!dest && !src)
		return (NULL);
	i = 0;
	dest2 = dest;
	src2 = src;
	while (i < n)
	{
		dest2[i] = src2[i];
		i++;
	}
	return (dest2);
}
